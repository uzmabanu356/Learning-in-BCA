// Start with a number
let number = 10; 
console.log("Starting number is: " + number); // 10

// Add 5 to the number
number += 5; // Same as number = number + 5
console.log("After adding 5: " + number); // 15

// Subtract 3 from the number
number -= 3; // Same as number = number - 3
console.log("After subtracting 3: " + number); // 12

// Multiply the number by 2
number *= 2; // Same as number = number * 2
console.log("After multiplying by 2: " + number); // 24

// Divide the number by 4
number /= 4; // Same as number = number / 4
console.log("After dividing by 4: " + number); // 6

// Find the remainder when dividing by 5
number %= 5; // Same as number = number % 5
console.log("After finding the remainder: " + number); // 1
